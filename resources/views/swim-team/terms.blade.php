                <dt>Facility and Pool Use:</dt>
                <dd>Aquatics Center Facility use is for Lincoln Aquatics Center customers only. Swim Team participants may use the locker rooms and reserved pool lanes during scheduled swim team practices and swim meets only, and must be supervised by an adult when inside the facility. Use of the locker rooms and pool outside of swim team time is for Lincoln Aquatics Center customers only during their operating hours. (The daily admission fee is $2.00 cash per person.)</dd>

                <dt>Swimmer Registration & Program Payment:</dt>
                <dd>All swimmers must register online following tryouts to reserve their spot on the swim team. The registration fee must be paid online in full prior to attending the first swim practice. All payments are non refundable. A recurring monthly program fee will be applied on the 1st of each month per swimmer based on their assigned practice level within the program. A multiple child discount of 10% will be applied to this recurring monthly fee when applicable. The monthly fees are based on the current practice schedule frequency and duration of each practice level. To discontinue the program and stop monthly recurring payments, an email indicating cancellation of the swim team must be sent to info@theswimschoolfl.com.</dd>

                <dt>Apparel/Equipment:</dt>
                <dd>Once the new swimmer registration fee is paid, each new swimmer will receive one team swimsuit, cap, t-shirt, water bottle and drawstring bag. We have a Parrish Bull Sharks online team store with items available for purchase for returning swimmers as well as parents. Equipment at swim practices, such as kick boards and pull buoys, will be supplied by the Parrish Bull Sharks Swim Team. You will be responsible for supplying your swimmer(s) with goggles, practice swimsuits & swim caps, and towels.</dd>

                <dt>Practice Groups:</dt>
                <dd>The swim team coaches reserve the right to change a swimmer's swim team practice group in accordance with their swimming abilities as necessary.</dd>

                <dt>Safety:</dt>
                <dd>All children must be accompanied by an adult and directly supervised before and after their swim practices.</dd>

                <dt>Practice Observation:</dt>
                <dd>In order to foster the best learning environment for each swimmer, the least amount of distraction is required. For swimmers to remain focused and fully attentive to their coaches during swim practices, parents and other family members are permitted on the pool deck but must view swimmers from the designated observation area. We ask that parents refrain from interrupting swimmers and coaches during practice times.</dd>
                
                <dt>Swim Meets</dt>
                <dd>The Parrish Bull Sharks swim meet schedule will be available on the swim team page of The Swim School website as soon as the dates, times and locations are confirmed. Swim meets take place on Saturdays occurring once per month during the school year (with the exception of January and August) and twice per month in June and July. Swim meet participation is optional but highly encouraged.</dd>
                
                <dt>Parent Volunteer Requirement:</dt>
                <dd>Swim meets only function with the help of parent volunteers. Parents of the Parrish Bull Sharks swim team are required to assist at swim meets in various roles including swimmer lineup, timing, etc. Training will be provided. One parent per swim family will be expected to either volunteer at the swim meets attending, or opt to pay a $100 non-volunteer fee per swimmer. The non-volunteer fee will be due prior to each swimmer's participation in their first swim meet if you choose this option in lieu of volunteering.</dd>
                
                <dt>Inclement Weather:</dt>
                <dd>In the event of inclement weather (i.e. lightning within 10.0 miles or less from pool location and/or heavy rain which limits visibility of the bottom of the pool), swim practices will be cancelled. In the event of cold weather (i.e. for the White 1 & 2 Teams, an air temperature of 60 degrees or below; and for the Gray & Blue Teams, an air temperature of 55 degrees or below), swim practices will be cancelled. The coach will contact you through text message via the Remind app when a practice needs to be cancelled due to inclement or cold weather. It is best to assume swim practices are on as scheduled if you are not contacted by the coach. If inclement weather occurs during practices, the lifeguards and coaches will remove all swimmers from the pool immediately and everyone will be required to vacate the Lincoln Aquatics Center Facility. We will make every effort to not cancel an entire swim meet, but the same policies will be followed for those as well.</dd>
                
                <dt>No Makeup Practices/Meets:</dt>
                <dd>Swim practices and swim meets will not be rescheduled or made up under any circumstances, including for cancellations due to inclement or cold weather. Program fees will not be prorated or refunded for cancelled swim practices or meets as a result of inclement or cold weather.</dd>

                <dt>Observed Holidays & Programs Breaks:</dt>
                <dd>The Parrish Bull Sharks Swim Team will observe major nationally celebrated holidays and will not have practices on these days. We will also take program breaks throughout the school year in accordance with Manatee County School District holidays, vacations, etc. as well as an annual break for the month of August. Program fees will not be prorated for scheduled breaks, including the month of August.</dd>

                <dt>Awards Banquet:</dt>
                <dd>At the conclusion of the summer, we will host an awards banquet (late August) where each swimmer will be recognized with awards to honor their achievements. Program fees for the month of August will go towards the cost of the awards banquet.</dd>

                <dt>Parent Conduct:</dt>
                <dd>The goal of the Parrish Bull Sharks Swim Team is to foster a positive environment for all involved. We will need the support and involvement of our parents to run an effective program. Just as we will expect the swimmers to practice teamwork, good sportsmanship, and self-control, this will be the same expected conduct for all parents as well. We encourage open communication but ask that parents refrain from interrupting swimmers and coaches during practice times. Coaches will make themselves available for pertinent discussion outside of practice times. As a parent, I understand the Parrish Bull Sharks Swim Team maintains the right to dismiss anyone from involvement with the team with or without cause if it brings discredit or discord to the program.</dd>

