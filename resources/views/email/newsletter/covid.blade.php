# 2021 Safety Precautions in Response to COVID-19
Please read our health & safety precautions for attending swim programs while at the River Wilderness Country Club. Please contact us with any questions.
- Face coverings are not required while outside on the pool deck but they are required for those 6 yrs & up if you go inside the facility to use the locker rooms.
- Please limit the number of adult family members accompanying each swim participant as much as possible.
- Please continue to practice physical distancing while entering and exiting the pool area, sitting on the pool deck, etc.
- The Swim School staff will continue to practice physical distancing with the swim participants as much as possible while also taking into consideration water safety needs and available pool space.
- If your swim participant becomes ill, please stay home until at least 24 hours after a fever is gone, without the use of fever-reducing medicines, and after symptoms have improved.
